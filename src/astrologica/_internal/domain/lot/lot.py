"""Lot enum — the seven Hellenistic lots."""

from __future__ import annotations

from enum import Enum


class Lot(Enum):
    """The seven Hellenistic lots (also called Arabic parts)."""

    FORTUNE = "fortune"
    SPIRIT = "spirit"
    EROS = "eros"
    NECESSITY = "necessity"
    COURAGE = "courage"
    VICTORY = "victory"
    NEMESIS = "nemesis"
